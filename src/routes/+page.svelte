	  <h2>Gauges: The Gateway to $BGT Rewards</h2>
	  <p>
		Gauges are the key mechanism through which $BGT rewards are distributed. Each gauge is associated with a specific PoL-eligible asset, and users who stake their assets in these gauges can earn $BGT rewards. The amount of rewards earned is determined by the user's share of the total assets staked in the gauge and the amount of $BGT directed to it.
		Validators play a crucial role in the distribution of $BGT rewards. They vote to allocate a portion of their $BGT emissions to specific gauges, thereby influencing the flow of rewards within the ecosystem. New gauges can be created through a governance process, allowing for the expansion and adaptation of the ecosystem as new assets become PoL-eligible.
	  </p>
